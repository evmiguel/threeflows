export const obfuscateEmail = (identifier) => {
  if (identifier === null) return '(null)';
  if (identifier === undefined) return '(undefined)';
  return hashInto(identifier, colorNames) + ':' + hashToHex(identifier).slice(-2);
}

export function hashInto(value, buckets) {
  const bucket = hashToInt(value);
  const index = bucket % buckets.length;
  return buckets[index];
}

// For communicating without using plain email address in querystrings.
// This is used client side as well, and so this is more obfuscation than
// security.
function hashToHex(str) {
  const shasum = crypto.createHash('sha256');
  shasum.update(str);
  return shasum.digest('hex');
}

// Hash a string to a 32-bit integer
function hashToInt(str) {
  return parseInt(hashToHex(str).slice(-8), 16);
}

